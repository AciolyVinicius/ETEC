    private void adiciona(String leitura) {
        boolean cheio = true;
        //adicionar o vetor e na JList
        for (int i = 0;i<cadastro.length;i++){
            if (cadastro [i] == null){
                cadastro [i]= leitura;
                cheio = false;
                break; //para aqui, se não grava o vetor todo
            }
        }
        if (cheio){
            JOptionPane.showMessageDialog(this, "Vagas indisponíveis");
        }
        limparCampos();
        atualizaLista(cadastro);
    }

    private void limparCampos() {
        txt_nome.setText(null);
        cmb_cursos.setSelectedIndex(-1);
        grupo_um.clearSelection();
        txt_nome.requestFocus();
    }

    private void atualizaLista(String [] vetor) {
         lista.clear();
         for(int i=0; i<vetor.length;i++){
             if(vetor[i]!=null){
                 lista.addElement(vetor[i]);
             }
         }
    }

    private void buscar(String nome) {
        String result = "";
        String dado = "";
        for (int i=0; i < cadastro.length;i++){
            if (cadastro[i]!=null){
                dado = cadastro[i].substring(0, cadastro[i].indexOf(','));
                if (dado.toUpperCase().contains(nome.toUpperCase())){
                    result+=dado+";\n";
                }
            }
        }
        String [] busca = result.split("\n"); //Posso usar ";"
        //Exibir os dados na JList
        atualizaLista(busca);
    }
