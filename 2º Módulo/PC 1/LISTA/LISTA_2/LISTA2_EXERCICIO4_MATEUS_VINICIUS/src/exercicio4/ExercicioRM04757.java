    private void atualizaLista() {
        DefaultListModel lista = new DefaultListModel();
        //For avançado
        //for((Tipo/Classe) (Variável de acesso a cada instância) : (ArrayList usado)){} 
        for(Cadastro x:listagem){
            lista.addElement("Código: "+ x.getCd() + " ; Data: " + x.getDia()+"/"+x.getMes() + "/" + x.getAno()+" ; Fracionamento: " + x.getFracionamento() + " ; Quantidade: " 
                    + x.getQuantidade()+ " ; Valor unitário: " + x.getValor());
        }
        lst_registros.setModel(lista);
        limpaCampos();
    }

    private void excluir(int cd) {
        boolean kill = false;
       //for((Tipo/Classe) (Variável de acesso a cada instância) : (ArrayList usado)){} 
       for (Cadastro k: listagem){//Excluir só a primeira ocorrência
           if ( cd == k.getCd()){
               listagem.remove(listagem.indexOf(k));
               kill = true;
               break;
           }
       }
        if(kill){
            atualizaLista();
            JOptionPane.showMessageDialog(this, "Cadastro excluído!");
        }
        else{
            JOptionPane.showMessageDialog(this, "Usuário não encontrado!");
        }
    }
