    private void btn_calcular_imcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcular_imcActionPerformed
        float peso = Float.parseFloat(txt_peso.getText()), altura = Float.parseFloat(txt_altura.getText());
        float imc;
        DecimalFormat df = new DecimalFormat("###,###,###.00");
        imc = peso / (altura * altura);
        String txtimc;
        if (imc<17){
            txtimc="Muito abaixo do peso";
        }
        else if (imc<18.5){
            txtimc="Abaixo do peso";
        }
        else if (imc<25){
            txtimc="Peso normal";
        }
        else if (imc<30){
            txtimc="Acima do peso";
        }
        else if (imc<35){
            txtimc="Obesidade grau I";
        }
        else if (imc<40){
            txtimc="Obesidade grau II (severa)";
        }
        else {
            txtimc="Obesidade grau III (mÃ³rbida)";
        }
        jd_imc.setSize(300,150);
        //centralizar a JDialog
        jd_imc.setLocationRelativeTo(this);
        
        jd_imc.setModal(true);
        //mandar o texto parab a JDialog
        lbl_imc.setText("IMC = "  + df.format(imc) + "\n " + txtimc);
                
        jd_imc.setVisible(true);
    }//GEN-LAST:event_btn_calcular_imcActionPerformed

