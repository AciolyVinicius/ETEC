    private void btn_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enterActionPerformed
        if(txt_senhaX.getText().length()==4){
            txt_senhaX.setEnabled(false);
            btn_enter.setEnabled(false);
            
            txt_senhaNOW.setEnabled(true);
            btn_enter_compare.setEnabled(true);
            
            finalpassword = txt_senhaX.getText();
            
        }
        else {
            JOptionPane.showMessageDialog(null, "Digite tudo corretamente!");
        }
    }//GEN-LAST:event_btn_enterActionPerformed

