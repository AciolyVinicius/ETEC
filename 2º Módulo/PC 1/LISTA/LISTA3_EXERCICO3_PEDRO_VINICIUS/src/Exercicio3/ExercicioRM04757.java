    private void btn_enter_compareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enter_compareActionPerformed
        if(count!=0){
            if(!txt_senhaNOW.getText().equals(finalpassword)){
                count--;
                if(count==0)
                {
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Senha incorreta! Você tem mais "+count +" chances!");
                }
            }
            else   {
                jd_projeto.setSize(600,500);
                jd_projeto.setLocationRelativeTo(this);
                jd_projeto.setModal(true);
                jd_projeto.setVisible(true);
            }
        }
    }//GEN-LAST:event_btn_enter_compareActionPerformed

