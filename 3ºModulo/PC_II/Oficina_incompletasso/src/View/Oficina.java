    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
         if (rdbBicicleta.isSelected()){
            Bicicleta b = new Bicicleta(Integer.parseInt(txtAro.getText()));
            b.setMarca(txtMarca.getText());
            b.setAro(Integer.parseInt(txtAro.getText()));
            b.setModelo(txtModelo.getText());
            servicoV.add(b);
            

         
         for(Bicicleta ba:servicoV)
         {
            if(ba instanceof Bicicleta)
            {
                saida+= "Bicicleta - Marca:"+ba.getMarca()+", "
                        + "Modelo:"+ ba.getModelo()
                        + "Aro:"+((Bicicleta)ba).getAro()+"\n";
            }
         }
         
         //Colocar os resultados do vetor no comboBox
         //cmbVeiculos.add(saida);
         //((CardLayout)pnlBase.getLayout()).show(pnlBase,"cardServico");
         
         txtVai.setText(saida);
         ((CardLayout)pnlBase.getLayout()).show(pnlBase,"cardServico");
            
        }
        if (rdbMoto.isSelected()){
            Motos m = new Motos(Integer.parseInt(txtCilin.getText()));
            m.setCilindradas(Integer.parseInt(txtCilin.getText()));
            m.setMarca(txtMarca.getText());
            m.setModelo(txtModelo.getText());
            servicoM.add(m);
            
            
         
         for(Motos ma:servicoM)
         {
            if(ma instanceof Motos)
            {
                saida+= "Moto - Marca:"+ma.getMarca()+", "
                        + "Modelo:"+ ma.getModelo()
                        + "Cilindrada:"+((Motos)ma).getCilindradas()+"\n";
            }
         }
         
         //Colocar os resultados do vetor no comboBox
         //cmbVeiculos.add(saida);
         //((CardLayout)pnlBase.getLayout()).show(pnlBase,"cardServico");
         
         txtVai.setText(saida);
         ((CardLayout)pnlBase.getLayout()).show(pnlBase,"cardServico");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

