    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(!novo){
            if(JOptionPane.showConfirmDialog(this, "Deseja excluir este contato?","Atenção", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            try{
                //Abrir a conexao
                con = Conexao.conectar("root","root");
                //pré formatar a string da query
                ps = con.prepareStatement("delete ftom tb_contato where telefone = ?");
                // definir os valores de cada índice (?)
                ps.setString(1, telefone);
                // executar a query
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Registro excluído!");
                novo = true;
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
            }
            }
        }
        else{
            String busca = JOptionPane.showInputDialog("Digite um nome ou telefone", "Busca");
        try{
            con = Conexao.conectar("root","root");
            ps = con.prepareStatement("select nome, telefone, tb_vinculo.tipo from tb_contato join tb_vinculo "
                    + "on tb_contato.tipo = tb_vinculo.codigo where nome like ? or telefone like ?");
            ps.setString(1, busca);
            ps.setString(2, busca);
            // receber os dados do banco
            rs = ps.executeQuery();
            // os dados estao em um bloco que precisa ser percorrido por um while
            txtRegistro.setText("");
            if(rs.next()){//enquanto houver um registro válido
                txtRegistro.append("Nome: "+rs.getString("nome")
                        +", Telefone: "+rs.getString("telefone")+", Tipo: "
                        + rs.getString("tipo")+"\n");
                txtNome.setText(rs.getString(1));
                txtTelefone.setText(rs.getString(2));
                cmbTipo.setSelectedItem(rs.getString(3));
                novo = false;
                telefone = rs.getString(2);
                JOptionPane.showMessageDialog(this, "Confirme os dados antes de excluir");
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
        }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

