    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if(novo){
            try{
                //Abrir a conexao
                con = Conexao.conectar("root","root");
                //pré formatar a string da query
                ps = con.prepareStatement("insert into tb_contato (telefone, nome, tipo)"
                        + "values (?,?,?)");
                // definir os valores de cada índice (?)
                ps.setString(1, txtTelefone.getText());
                ps.setString(2, txtNome.getText());
                ps.setInt(3, cmbTipo.getSelectedIndex()+1);
                // executar a query
                ps.execute();
                JOptionPane.showMessageDialog(this, "Dados Gravados");            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
            }
        }
        else{
            try{
                //Abrir a conexao
                con = Conexao.conectar("root","root");
                //pré formatar a string da query
                ps = con.prepareStatement("update tb_contato set telefone = ?, "
                        + "nome = ?, tipo = ?)"
                        + "where telefone = ?");
                // definir os valores de cada índice (?)
                ps.setString(1, txtTelefone.getText());
                ps.setString(2, txtNome.getText());
                ps.setInt(3, cmbTipo.getSelectedIndex()+1);
                ps.setString(4, telefone);
                // executar a query
                ps.execute();
                novo = true;
                JOptionPane.showMessageDialog(this, "Dados Gravados");            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListaActionPerformed
        txtRegistro.setText("");
        try{
            con = Conexao.conectar("root","root");
            ps = con.prepareStatement("select nome, telefone, tb_vinculo.tipo from tb_contato join tb_vinculo "
                    + "on tb_contato.tipo = tb_vinculo.codigo");
            // receber os dados do banco
            rs = ps.executeQuery();
            // os dados estao em um bloco que precisa ser percorrido por um while
            while(rs.next()){//enquanto houver um registro válido
                txtRegistro.append("Nome: "+rs.getString("nome")+", Telefone: "+rs.getString("telefone")+", Tipo: "+rs.getString("tipo")+"\n");
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
        }
    }//GEN-LAST:event_btnListaActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String busca = JOptionPane.showInputDialog("Digite um nome ou telefone", "Busca");
        try{
            con = Conexao.conectar("root","root");
            ps = con.prepareStatement("select nome, telefone, tb_vinculo.tipo from tb_contato join tb_vinculo "
                    + "on tb_contato.tipo = tb_vinculo.codigo where nome like ? or telefone like ?");
            ps.setString(1, "%"+busca+"%");
            ps.setString(2, "%"+busca+"%");
            // receber os dados do banco
            rs = ps.executeQuery();
            // os dados estao em um bloco que precisa ser percorrido por um while
            txtRegistro.setText("");
            if(rs.next()){//enquanto houver um registro válido
                txtRegistro.append("Nome: "+rs.getString("nome")
                        +", Telefone: "+rs.getString("telefone")+", Tipo: "
                        + rs.getString("tipo")+"\n");
                txtNome.setText(rs.getString(1));
                txtTelefone.setText(rs.getString(2));
                cmbTipo.setSelectedItem(rs.getString(3));
                novo = false;
                telefone = rs.getString(2);
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(this, "Erro: "+ex.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

